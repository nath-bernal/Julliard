    private void agregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_agregarActionPerformed
        // TODO add your handling code here:

        if (campoNumDocumento.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Por favor ingresa un número de documento para continuar.\nEn caso de que lo hayas hecho, verifica si tiene registro.", "Error", JOptionPane.WARNING_MESSAGE);
            return; // Salimos del método si el campo de documento está vacío
        }
        
        
        
        // Verificar si los campos están vacíos
            
        if (campoNumDocumento.getText().isEmpty() ||
            campoNombre.getText().isEmpty() ||
            campoEdad.getText().isEmpty() ||
            campoTipoDocumento.getSelectedItem() == null ||
            campoEmail.getText().isEmpty() ||
            campoCelular.getText().isEmpty()) {
            
        
            // Mostrar mensaje de advertencia
            JOptionPane.showMessageDialog(this, "Asegúrate de que ningún campo esté vacío.\nRecuerda que todos los campos son obligatorios.", "Advertencia", JOptionPane.WARNING_MESSAGE);
            return; // Salir del método si hay campos vacíos
        }
    
    
        List<String> errores = new ArrayList<>();

        String numDocumento = campoNumDocumento.getText();
        String nombre = campoNombre.getText();
        //int edad = Integer.parseInt(campoEdad.getText());
        String tipoDocumento = (String) campoTipoDocumento.getSelectedItem();
        String email = campoEmail.getText();
        String celular = campoCelular.getText();
        boolean estado = true;
        String curso = "";
        String evento = ""; 


        if (nombre.isEmpty() || campoEdad.getText().isEmpty() || 
            tipoDocumento == null || numDocumento.isEmpty() || 
            email.isEmpty() || celular.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Ningún campo puede estar vacío.", 
                "Mensaje", JOptionPane.ERROR_MESSAGE);
            return;
        }

        int edad;
        try {
            edad = Integer.parseInt(campoEdad.getText());
        } catch (NumberFormatException e) {
            edad = -1;
        }
        if (!Validador.nombreValido(nombre)) {
            errores.add("El nombre solo puede contener letras y espacios.");
        }
        if (!Validador.edadValida(edad)) {
            errores.add("La edad debe ser un número positivo entre 9 y 50.");
        }
        if (!Validador.numDocumentoValido(numDocumento)) {
            errores.add("El número de documento debe contener 10 dígitos (números).");
        }
        if (!Validador.correoValido(email)) {
            errores.add("El correo tiene un formato inválido (ej: usuario@example.com).");
        }
        if (!Validador.celularValido(celular)) {
            errores.add("El celular debe tener exactamente 10 dígitos (números).");
        }


        // Mostrar errores si existen
        if (!errores.isEmpty()) {
            JOptionPane.showMessageDialog(this, String.join("\n", errores), 
                "Errores de validación", JOptionPane.ERROR_MESSAGE);
            return;
        }
        Usuario nuevosDatos = new Usuario(nombre, edad, tipoDocumento, numDocumento, email, celular, estado, curso, evento);
        GestionDatos gestion = new GestionDatos();
        gestion.leerCSV("RegistroUsuario.csv"); // Cargar usuarios

        if (gestion.actualizarUsuario(numDocumento, nuevosDatos)) {
            JOptionPane.showMessageDialog(this, "¡Su información fue actualizada con éxito!");
            gestion.guardarEnCSV("RegistroUsuario.csv"); // Guardar los cambios en el CSV
        } else {
        JOptionPane.showMessageDialog(this, "Usuario no encontrado para actualizar.");
        }  
                                    
    }//GEN-LAST:event_agregarActionPerformed

