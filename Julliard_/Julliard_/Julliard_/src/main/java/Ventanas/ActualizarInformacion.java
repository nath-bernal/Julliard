    private void buscarButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buscarButtonActionPerformed
        // TODO add your handling code here:
        
        
        String numDocumento = campoBuscarID.getText();
        GestionDatos gestion = new GestionDatos();
        
        if (numDocumento.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Por favor ingresa un número de documento para buscar.", "Campo Vacío", JOptionPane.WARNING_MESSAGE);
            return;
        }
        
            // Validar que el número de documento tenga exactamente 10 dígitos
        if (numDocumento.length() != 10 || !numDocumento.matches("\\d+")) {
            JOptionPane.showMessageDialog(this, "Recuerda que el número de documento solo contiene 10 dígitos.", "Error de Validación", JOptionPane.ERROR_MESSAGE);
            return; // Salimos del método si el documento no es válido
        }
        
        gestion.leerCSV("RegistroUsuario.csv");  

        Usuario usuario = gestion.buscarID(numDocumento);

        if (usuario != null) {
            
            // Mostrar los datos del usuario en los JTextField
            campoNombre.setText(usuario.getNombre());
            campoEdad.setText(String.valueOf(usuario.getEdad()));
            campoTipoDocumento.setSelectedItem(usuario.getTipoDocumento());
            campoNumDocumento.setText(usuario.getNumeroDocumento());
            campoEmail.setText(usuario.getEmail());
            campoCelular.setText(usuario.getCelular());
                 
            
        } else {
            JOptionPane.showMessageDialog(this, "No se encontró ningún usuario con el documento ingresado.", "Usuario No Encontrado", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_buscarButtonActionPerformed

