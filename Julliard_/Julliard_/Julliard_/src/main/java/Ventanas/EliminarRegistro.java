    private void buscarButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buscarButtonActionPerformed
        // TODO add your handling code here:
        String numeroDocumento = campoNumDocumento.getText();
        String curso = (String) campoCurso.getSelectedItem();
        GestionDatos gestion = new GestionDatos();
 
  
        gestion.leerCSV("RegistroUsuario.csv");  
        
        if (numeroDocumento.isEmpty()) {
            JOptionPane.showMessageDialog(this, "El campo de número de documento no puede estar vacío.");
            return;
        }
        
        // Validar que el número de documento tenga exactamente 10 dígitos
        if (numeroDocumento.length() != 10 || !numeroDocumento.matches("\\d+")) {
            JOptionPane.showMessageDialog(this, 
                "Recuerda que el número de documento solo contiene 10 dígitos.", 
                "Error de Validación", 
                JOptionPane.ERROR_MESSAGE);
            return; // Salimos del método si el documento no es válido
        }
        
        Usuario usuario = gestion.buscarParaEliminar(numeroDocumento, curso);

        if (usuario != null) {
            // Mostrar los datos del usuario en los JTextField
            campoCuadro.setText(usuario.getNombre());
        } else {
            JOptionPane.showMessageDialog(this, "Usuario no encontrado.\nNo hay registro asociado al número de documento y curso proporcionados.");
        }
    }//GEN-LAST:event_buscarButtonActionPerformed

