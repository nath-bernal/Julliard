    private void eliminarRegistroButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_eliminarRegistroButtonActionPerformed
        
        // TODO add your handling code here:
        String numDocumento = campoNumDocumento.getText();
        String curso = (String) campoCurso.getSelectedItem();

        // Validar que los campos no estén vacíos
        if (numDocumento.isEmpty() || curso == null || curso.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Por favor, complete todos los campos antes de eliminar.");
            return; // Salir del método si hay campos vacíos
        }
        
        GestionDatos gestion = new GestionDatos();
        gestion.leerCSV("RegistroUsuario.csv");  
        
        // Verificar si el usuario existe antes de intentar eliminarlo
        Usuario usuario = gestion.buscarParaEliminar(numDocumento, curso);
        if (usuario == null) {
            JOptionPane.showMessageDialog(this, "No se puede eliminar. El usuario no se encuentra registrado.");
            return; // Salir del método si el usuario no existe
        }

        
        gestion.eliminarUsuario(numDocumento, curso);
        JOptionPane.showMessageDialog(this, "¡Su información fue eliminada con éxito!");
        

    }//GEN-LAST:event_eliminarRegistroButtonActionPerformed

